import UIKit

extension UIColor {
    static func delicatePinkColor(alpha: Double = 1.0) -> UIColor {
        return UIColor(
            red: CGFloat(230) / 255.0,
            green: CGFloat(90) / 255.0,
            blue: CGFloat(173) / 255.0,
            alpha: CGFloat(alpha)
        )
    }
    static func fuchsiaColor(alpha: Double = 1.0) -> UIColor {
        return UIColor(
            red: CGFloat(193) / 255.0,
            green: CGFloat(84) / 255.0,
            blue: CGFloat(193) / 255.0,
            alpha: CGFloat(alpha)
        )
    }
    static func pinkColor(alpha: Double = 1.0) -> UIColor {
        return UIColor(
            red: CGFloat(213) / 255.0,
            green: CGFloat(0) / 255.0,
            blue: CGFloat(101) / 255.0,
            alpha: CGFloat(alpha)
        )
    }
    static func vividPinkColor(alpha: Double = 1.0) -> UIColor {
        return UIColor(
            red: CGFloat(213) / 255.0,
            green: CGFloat(0) / 255.0,
            blue: CGFloat(101) / 255.0,
            alpha: CGFloat(alpha)
        )
    }
}
